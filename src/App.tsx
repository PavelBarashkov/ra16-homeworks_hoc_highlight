import React, { useState } from 'react';
import { withConditionalRendering } from './HOC';

export function New(props: any) {
    return (
        <div className="wrap-item wrap-item-new">
            <span className="label">New!</span>
            {props.children}
        </div>
    )
};

export function Popular(props: any) {
    return (
        <div className="wrap-item wrap-item-popular">
            <span className="label">Popular!</span>
            {props.children}
        </div>
    )
};

function Article(props: any) {
    return (
        <div className="item item-article">
            <h3><a href="#">{props.title}</a></h3>
            <p className="views">Прочтений: {props.views}</p>
        </div>
    )
};

function Video(props: any) {
    return (
        <div className="item item-video">
            <iframe src={props.url} frameBorder="0" allow="autoplay; encrypted-media" allowFullScreen></iframe>
            <p className="views">Просмотров: {props.views}</p>
        </div>
    )
};

function List(props: any) {
    return props.list.map((item: any) => {
        if (item.type === 'video') {
            const ConditionalVideo  = withConditionalRendering(Video, item.views)
            return <ConditionalVideo key={item.views} {...item} />
        }
        if (item.type === 'article') {
            const ConditionalArticle  = withConditionalRendering(Article, item.views)
            return <ConditionalArticle key={item.views}{...item} />
        }
        
    });
};

